for line in POKEMON.splitlines():
    parts = line.split("\t")
    name_en = parts[3]
    name_en = name_en.replace(" ", "").replace("-", "").lower()
    pokemon_names[name_en] = parts[1]

def meta3():

    O, D, T = Ints('O D T')

    s = Solver()

    # T/2 必须是整数，所以 T 必须是偶数
    s.add(T % 2 == 0)

    # 定义6个表达式
    e1 = O + T
    e2 = T / 2
    e3 = O * D - T / 2
    e4 = O * D
    e5 = D * D - D
    e6 = O * O

    # 每个表达式的结果在 [1, 26] 之间
    for e in [e1, e2, e3, e4, e5, e6]:
        s.add(e >= 1, e <= 26)

    # 枚举所有解
    results = []
    while s.check() == sat:
        m = s.model()
        o_val = m[O].as_long()
        d_val = m[D].as_long()
        t_val = m[T].as_long()
        results.append((o_val, d_val, t_val))
        # 排除当前解，继续搜索
        s.add(Or(O != o_val, D != d_val, T != t_val))

    # 输出结果
    print(f"共找到 {len(results)} 组解\n")
    string_result = []
    for o_val, d_val, t_val in sorted(results):
        t_half = t_val // 2
        od = o_val * d_val

        name = ''.join(chr(val + ord('a') - 1) for val in [o_val + t_val, t_half, od - t_half, od, d_val**2 - d_val, o_val**2])
        string_result.append(name)
        if name in pokemon_names:
            print(f"Found a match: {name}")
            print(f"O={o_val}, D={d_val}, T={t_val}")
            print(f"  ① O+T      = {o_val + t_val}")
            print(f"  ② T/2      = {t_half}")
            print(f"  ③ O*D-T/2  = {od - t_half}")
            print(f"  ④ O*D      = {od}")
            print(f"  ⑤ D²-D     = {d_val**2 - d_val}")
            print(f"  ⑥ O²       = {o_val**2}")
            print()


def single_wordle(test, answer):
    result = ''
    for i in range(len(test)):
        if test[i] == answer[i]:
            result += 'G'
        elif test[i] in answer:
            result += 'Y'
        else:
            result += 'W'
    return result

